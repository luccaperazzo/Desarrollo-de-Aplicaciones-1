package ar.edu.uade.recipes.model;

public class RegisterResponse {
    private String message;
    private String id;
    private String email;
    private String username;

    public String getMessage() {
        return message;
    }

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getUsername() {
        return username;
    }
}

